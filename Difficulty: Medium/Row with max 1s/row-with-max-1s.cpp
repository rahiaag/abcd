// User function template for C++
class Solution {
  public:
    int rowWithMax1s(vector<vector<int>> &arr) {
        int n=arr.size();
        int m=arr[0].size();
        
        for (int i=0;i<m;i++){
            
            for (int j=0;j<n;j++){
                {
                    if (arr[j][i]==1){
                        return j;
                    }
                    
                }
            }
        }
        return -1;
    }
};

// Question say which contain the maximum number of 1 but we dont need to count it as it is sorted 
// each row is sorted

